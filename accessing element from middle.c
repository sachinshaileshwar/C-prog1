#include <stdio.h>
#include <string.h>
int main()
{
    char str[100], sstr[100];
    int i=0,n,j=0,m;
    printf("enter the string:");
    scanf("%s", str);
    printf("enter the start value:");
    scanf("%d", &n);
    printf("Enter the end value:");
    scanf("%d",&m);
    i=m;
    while(str[i]!='\0' && n>0)
    {
        sstr[j]=str[i];
        i++;j++;n--;
    }
    sstr[i]='\0';
    printf("\n The substring is:");
    puts(sstr);
    return 0;
}
